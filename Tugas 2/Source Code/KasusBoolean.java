public class KasusBoolean {
    public static void main(String[] args) {
        boolean bool = true;

        if (bool) {
            System.out.println("true");
        } else {
            System.out.println("false");
        }

        if (!bool) {
            System.out.println("salah");
        } else {
            System.out.println("benar");
        }
    }
}
